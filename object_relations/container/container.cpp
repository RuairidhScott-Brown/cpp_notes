/*
CONTAINER
    Container class is a class designed to hold and organize multiple
    instances of another type (either another class, or a fundamental type).

    typically only hold one type of object.


CONTAINER FUNCTIONALITY
    - Create an empty container (via a constructor)
    - Insert a new object into the container
    - Remove an object from the container
    - Report the number of objects currently in the container
    - Empty the container of all objects
    - Provide access to the stored objects
    - Sort the elements (optional) 

VALUE CONTAINERS
    - composition classes that store copies of the object that we want to hold
    - responsible for creating and destroying the copies

REFERENCE CONTAINERS
    - aggregations
    - store pointers or references to the variables.

*/